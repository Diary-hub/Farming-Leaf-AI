  }

  GestureDetector save() {
    return GestureDetector(
      onTap: () async {
        // print(selctedItemi); //type
        // print(amount_c.text); //money
        // print(date); //date
        // print(expalin_C.text); //tip
        // print(selctedItem!.name); //name

        // var add = Add_data(selctedItemi!, amount_c.text, date, expalin_C.text, selctedItem!);
        // box.add(add);
        await addData(
            date: date,
            money: double.parse(amount_c.text),
            plant: selctedItem!,
            tip: expalin_C.text,
            type: selctedItemi);
        Navigator.of(context).pop();
      },
