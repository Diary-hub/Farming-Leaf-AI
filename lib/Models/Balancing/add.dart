class Add_Screen extends StatefulWidget {
  final BalanceItem? plant;
  const Add_Screen({Key? key, this.plant}) : super(key: key);

  @override
  State<Add_Screen> createState() => _Add_ScreenState();
}

class _Add_ScreenState extends State<Add_Screen> {
  final box = Hive.box<Add_data>('data');
  DateTime date = new DateTime.now();
  Plant? selctedItem;
  String? selctedItemi;
  final TextEditingController expalin_C = TextEditingController();
  FocusNode ex = FocusNode();
  final TextEditingController amount_c = TextEditingController();
  FocusNode amount_ = FocusNode();

  bool isData = false;

  final List<String> _itemei = [
    'داهات',
    "خەرجی",
  ];
  List<Plant> __plantList = Plant.plantList + Plant2.plantList + Plant3.plantList;
  void initState() {
    super.initState();
    ex.addListener(() {
      setState(() {});
    });
    amount_.addListener(() {
      setState(() {});
    });

    isData = widget.plant != null ? true : false;

    if (isData) {
      selctedItem = __plantList.where((element) => element.plantId == widget.plant!.plantId).first;
      selctedItemi = _itemei.where((element) => element == widget.plant!.type).first;
      amount_c.text = widget.plant!.money.toString();
      expalin_C.text = widget.plant!.tip;
    }
  }

  String generateRandomString(int length) {
    const characters = 'abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';
    Random random = Random();

    return String.fromCharCodes(
      Iterable.generate(
        length,
        (_) => characters.codeUnitAt(random.nextInt(characters.length)),
      ),
    );
  }

  Future<void> addData(
      {required Plant plant,
      required money,
      required DateTime date,
      required type,
      required tip}) async {
    try {
      FirebaseFirestore db = FirebaseFirestore.instance;

      final item = BalanceItem(
          plantId: plant.plantId,
          date: date.toString(),
          name: plant.name,
          imageURL: plant.imageURL,
          money: type == 'داهات' ? money : (money * -1),
          tip: tip,
          type: type);

      await db
          .collection('Users')
          .doc(FirebaseAuth.instance.currentUser!.uid)
          .collection('Sales')
          .doc(isData ? widget.plant!.id : generateRandomString(10))
          .set(item.toJson());
      print("Item Added In Balance");
    } catch (e) {
      print("Firebase Error: $e");
    }
  }

  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: Colors.grey.shade100,
      body: SafeArea(
        child: Stack(
          alignment: AlignmentDirectional.center,
          children: [
            background_container(context),
            Positioned(
              top: 120,
              child: main_container(),
            ),
          ],
        ),
      ),
    );
  }

  Container main_container() {
    return Container(
      decoration: BoxDecoration(
        borderRadius: BorderRadius.circular(20),
        color: Colors.white,
      ),
      height: 550,
      width: 340,
      child: Column(
        children: [
          SizedBox(height: 50),
          name(),
          SizedBox(height: 30),
          explain(),
          SizedBox(height: 30),
          amount(),
          SizedBox(height: 30),
          How(),
          SizedBox(height: 30),
